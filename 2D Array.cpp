#include<iostream>

using namespace std;

int main()
{
    int n,m;
    cout<<"Enter the Number of the Row & Column"<<endl;
    cin>>n>>m;
    cout<<endl;
    int B[10][10];
    for (int i = 0; i < n; i++)     // For the Input Of Row
    {
        for (int j = 0; i < m; i++)        // For the Input Of Column
        {
            cin>>B[i][j];
        }
        
    }
    

    return 0;
}
